#include <stdio.h> //inclusão da biblioteca de dados stdio.h

int main () //função principal da linguagem
{
	char letra = 'a';  //tipo char (um caractere somente)
	int num1 = 10;	   //tipo int (inteiros)
	float num2 = 10.5; //tipo float (reais)
	
	printf ("\n%c", letra); //saída de dados 
	printf ("\n%i", num1);
	printf ("\n%f", num2);
	
	return 0;
}


